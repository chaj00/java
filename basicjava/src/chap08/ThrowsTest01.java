package chap08;
//예외 발생하는 곳에서 무조건 예외를 처리하는 예제
public class ThrowsTest01 {
	//a()가 예외발생 가능성이 있는 코드를 가지고 있는 메소드로 정의
	//==> 예외가 발생하면 무조건  a()에서 처리
	public int a(int num1, int num2){
		System.out.println("********a()호출********");
		int result=0;
		try{
			System.out.println("실행중");
			result =num1/num2;
			System.out.println("실행완료");
		}catch(ArithmeticException e){
			result =100000; //오류가 발생하면 무조건 result에 10000을 할당
		}
		return result;
	}
	//a()를 호출하는 b()를 정의
	public void b(int num1,int num2){
		System.out.println("********b()호출********");
		int result = a(num1,num2);
		System.out.println("a()호출결과===>"+result);
	}
	//a()를 호출하는 c()를 정의
		public void c(int num1,int num2){
			System.out.println("********c()호출********");
			int result = a(num1,num2);
			System.out.println("a()호출결과===>"+result);
		}
	public static void main(String[] args) {
		ThrowsTest01 obj = new ThrowsTest01();
		
		obj.b(1000,0);
		obj.c(10, 0);
		
		

	}

}
